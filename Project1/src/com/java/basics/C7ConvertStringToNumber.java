package com.java.basics;

public class C7ConvertStringToNumber {

	public static void main(String[] args) {


		String strNum1 = "100";
		
		String strNum2 = "200";
	
		//final variables acts like constants
		//you cannot chage the value of final variable
		final int n1 = Integer.parseInt(strNum1);
		
		System.out.println(n1);
		
		System.out.println(n1);
		int n2 = Integer.parseInt(strNum2);
		
		System.out.println(strNum1+strNum2);
		System.out.println(n1+n2);
		
		
	}

}
