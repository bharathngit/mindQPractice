package com.java.basics;

public class C4Datatypes {

	public static void main(String[] args) {

		byte b = 127;
		System.out.println("the byte value is " + b);
		short s = 32000;
		System.out.println("the short value is " + s);

		int i = 215000000;
		System.out.println("the int value is " + i);

		long ln = 10000000000L;
		System.out.println("the long value is " + ln);

		float f = 1.123456789f;
		System.out.println("the floating value is " + f);

		double d = 1.12345678912345123456;
		System.out.println("the double value is " + d);

		char c = 'a';
		System.out.println("the character is " + c);

		boolean bl = false;
		System.out.println("is element there :" + bl);
		
	}

}



